
# 99 bottles of beer on the wall.
# Demonstrates classic for loop decrement

puts '99 bottles of beer on the wall'

for i in (99).downto(1)
  puts "take one down, pass it around, #{i} bottles of beer on the wall"
end

puts "No more bottles of beer on the wall, no more bottles of beer. Go to the store and buy some more, 99 bottles of beer on the wall..."
